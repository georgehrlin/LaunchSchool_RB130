=begin
A testing framework is software that provides a way to test each of the
components of an application.The framework should be able to: provide
appropriate inputs, check return values, examine outputs, and even determine if
errors occur when they should.

The three basic features of testing frameworks:
1. A way to describe the tests one wants to run
2. A way to execute those tests
3. A way to report the results of these tests
=end

=begin
General Pattern of Test-Driven Development (TDD)
1. Create a test that fails.
2. Write just enough code to implement the change or new feature.
3. Refactor and improve things, then repeat tests.
Also called Red-Green-Factor.
=end
